export function buildReadinessDetail(visit: DoctorWorklistVisit): VisitReadinessDetail {
  const hasBlocking = visit.blockingGapCount > 0;
  return {
    visit,
    version: "v1.4",
    evaluatedAt: "2026-07-13 10:18",
    sourceLinked: true,
    readyThreshold: 85,
    breakdown: breakdown.map((item) => ({
      ...item,
      blocking: item.id === "evidence" ? hasBlocking : item.blocking && hasBlocking,
    })),
    scoreChanges,
    priorityGaps: hasBlocking
      ? [
          {
            id: "GAP-001",
            severity: visit.riskLevel === "Critical" ? "Blocking" : "Warning",
            title: visit.primaryGap ?? "Readiness evidence incomplete",
            explanation: "Required source evidence or clinical rationale must be completed using approved source data.",
            owner: "Doctor",
            dueTime: "Today 12:00",
            source: "SOAP Note v3 / Evidence checklist",
            recommendedAction: visit.nextAction,
          },
        ]
      : [
          {
            id: "GAP-READY",
            severity: "Information",
            title: "No blocking gaps detected",
            explanation: "Configured readiness checks are complete. Human verification remains mandatory.",
            owner: "Claim Reviewer",
            dueTime: "Today 16:00",
            source: "Readiness evaluation v1.4",
            recommendedAction: "Reviewer validation",
          },
        ],
    auditTrail: baseAudit,
    sources: [
      { id: "SRC-001", source: "SOAP Note", version: "v3", usedFor: "Clinical completeness", status: "Source-linked" },
      { id: "SRC-002", source: "Diagnosis Record", version: "v2", usedFor: "ICD consistency", status: "Clinician verification required" },
      { id: "SRC-003", source: "Payer Rule Reference", version: "2026.06", usedFor: "Evidence requirements", status: "Configured MVP rule" },
      { id: "SRC-004", source: "Visit Cost Summary", version: "v1", usedFor: "Cost review", status: "Review" },
    ],
  };
}

